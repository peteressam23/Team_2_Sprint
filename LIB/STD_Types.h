#ifndef STD_H_
#define STD_H_

/*********************************************Macros of Data Types*********************************************************/


typedef unsigned char       u8;             // 1 byte unsigned char
typedef signed   char       s8;            // 1 byte signed char
typedef unsigned short int  u16;          // 2 byte unsigned int
typedef signed   short int  s16;         // 2 byte signed int
typedef unsigned long int   u32;        // 4 byte unsigned long int
typedef signed   long int   s32;       // 4 byte signed long int
typedef float               f32;      // 4 byte float
typedef double              f64;     // 8 byte  double
#define NULL 			     0
#define NULL_PTR		  ((void*)0)




#endif /* STD_H_ */